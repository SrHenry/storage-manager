#!/usr/bin/env node

/**
 * Scans src/adapters/ for adapter subdirectories containing register.ts
 * and generates src/adapters/auto-register.ts with side-effect imports.
 *
 * Convention: each adapter lives in src/adapters/<runtime>/ and must
 * provide a register.ts that self-registers with the runtime resolver.
 *
 * This script runs at build time so the source tree stays decoupled —
 * adding a new adapter directory is the only step needed.
 */
import { readdirSync, statSync, writeFileSync } from 'node:fs'
import { join, relative } from 'node:path'
import { fileURLToPath } from 'node:url'

const ROOT = fileURLToPath(new URL('..', import.meta.url))
const ADAPTERS_DIR = join(ROOT, 'src', 'adapters')
const OUTPUT = join(ADAPTERS_DIR, 'auto-register.ts')

const KNOWN_DIRS = new Set(['interfaces', '__tests__'])

const adapterDirs = readdirSync(ADAPTERS_DIR)
    .filter(entry => {
        if (KNOWN_DIRS.has(entry)) return false
        const full = join(ADAPTERS_DIR, entry)
        return statSync(full).isDirectory()
    })
    .filter(entry => {
        const registerFile = join(ADAPTERS_DIR, entry, 'register.ts')
        try {
            return statSync(registerFile).isFile()
        } catch {
            return false
        }
    })

const lines = [
    '// Auto-generated by scripts/generate-adapter-registrations.mjs',
    '// Do not edit — regenerate with: node scripts/generate-adapter-registrations.mjs',
    '',
]

for (const dir of adapterDirs) {
    lines.push(`import './${dir}/register'`)
}

lines.push('')

writeFileSync(OUTPUT, lines.join('\n'), 'utf8')

const rel = relative(ROOT, OUTPUT)
if (adapterDirs.length === 0) {
    console.log(`${rel}: no adapter registrations found`)
} else {
    console.log(`${rel}: registered ${adapterDirs.join(', ')}`)
}
